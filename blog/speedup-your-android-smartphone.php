                                <h2>How to enhance performance of <a>Android Phone</a> ? </h2>
                                <p>Your Android phone was probably fast when you first bought it? Then over time it began running more slowly. 
                                    It's a quite common problem that sometime you may have asked someone about it. Downloading applications, videos, 
                                    taking pictures etc can surely slow down your smartphone with time. Even it can be that much slow that letters are 
                                    coming a second or two after your type them. But fortunately nothing to worry in such case. 
                                    You just need to follow few simple steps to make your Smartphone smarter than before: 
                                </p>
                                <ol>
                                    <li><b>Identify the cause</b>
                                        <p>
                                            Find out what is causing the problem: an app, multiple apps, the system itself ? There can be thousands of 
                                            reasons why your device is slowing down, sometime the slowness can be fixed by following a few simple tips
                                            and tricks.
                                        </p>
                                        
                                    </li>
                                    <li><b>Uninstall some apps</b>
                                        <p>
                                            Remove those apps that you rarely use. We download apps, use them twice or thrice and then forget they are
                                            even there. These apps will slow down your phone by using resources like CPU Cycles or taking up memory. 
                                            To uninstall apps you just need to press on the app listed that you want to delete. A screen will appear 
                                            that will give some details about the app, including how much storage the app consumes, how much of information is stored in your cache file and any default launch 
                                            settings.Once you are certain that you have selected the correct app and that you want to delete it from your phone, press
                                            the "Unistall" button. This will begin to delete the app from your device. Once finished deleting, your screen will show 
                                            that the app has been deleted. 
                                        </p>
                                        
                                    </li>
                                    <li><b>Clear the Cache</b>
                                        <p>
                                            Many apps that you have installed use data caches to store data in temporary storage in an attempt to speed up operations.
                                            This is effective up to a point, but if you are running short of space, it can actually be counter-productive. 
                                            You just need to go to the settings, then to profile area for each individual app present in your Smartphone. 
                                            To do this, go to the storage section of Settings on your device. In 4.2 and up, you'll see a new item called 
                                            "Cached data". Tapping this will give you the option to erase everything. While this may have some unintended 
                                            effects like making things take longer to load or forcing you to re-login to apps, it can help performance to do so 
                                            once in a while.
                                        </p>
                                    </li>
                                    <li><b>Check your SD Card</b>
                                        <p>
                                            It is better to move few large files to external memory like SD card. You should try to transfer video files, mp3 etc. 
                                            to your SD card to provide some free space to your Smartphone’s internal memory. If your phone is starved for internal 
                                            storage and you can't spare any of your hard-earned apps, you can try moving them to your SD card instead. This will free 
                                            up that internal space and speed up your phone, but keep all your apps close at hand. To do this, just head to Settings > 
                                            Applications > Manage Applications. Select an app, and tap the "Move to SD Card" button to move it. Some apps won't have 
                                            this ability, but you should find that lots of your space-hogging apps have no problem living on your SD card.
                                        </p>
                                    </li>
                                    <li><b>Disable Animations and Extras</b>
                                        <p>
                                            Animations look nice when navigating your phone or tablet, but they can cause delays and even slow down lower-powered 
                                            devices. Depending on the launcher you are using, there are various animations and special effects in place. These may 
                                            look great, but they can also slow things down. Check in your launcher's settings to see if there is a way to disable any 
                                            of these unnecessary extras, and you could earn yourself a speed boost. To disable animations Make sure developer options 
                                            are enabled. If they're not, go to Settings > About phone, then tap on Build number several times to enable it. Then go to
                                            Settings > Developer options, and scroll down to Window animation scale, Transition animation scale, and Animator duration
                                            scale. Tap on each of the animation options and turn them off. Remember to leave Developer options turned on to keep the 
                                            animations disabled. Turning Developer options off will reset the animations back to their default settings.
                                        </p>
                                    </li>
                                    <li><b>Free up your RAM</b>
                                        <p>
                                            Multi-tasking several apps make it easy to switch between different tools, but that can also be cause of your phone’s
                                            bad performance. You should quickly close down apps you are no longer using by calling up the running apps list – press
                                            and hold the home key and then swipe away any apps you want to exit.
                                        </p>
                                    </li>
                                    <li><b>Factory Reset</b>
                                        <p>
                                            If nothing works, you should reset your phone. Before resetting you should backup your data. On your mobile device, 
                                            open the Settings menu. Under "Personal," touch Backup & reset. You may need to enter your pattern, PIN, or password. 
                                            Under "Personal data," touch Factory data reset. Read the information on the screen and touch Reset phone. 
                                            If you have a screen lock, you'll need to enter your pattern, PIN, or password. When prompted, touch Erase everything to 
                                            erase all data from your device's internal storage. When your device has finished erasing, select the option to reboot 
                                            your device. You'll see the Welcome screen just as you did when you turned the device on for the first time. 
                                            When you're done with the setup process, follow these steps to restore your data.
                                        </p>
                                        <p>
                                            To do a factory data reset, follow these steps.If your phone is on, turn it off. Press and hold the Volume up button and 
                                            the Power button at the same time until the device turns on. Press the Volume down button to select "Recovery mode." 
                                            If you need to navigate to the "Recovery mode" option, use the Volume up button to move the arrow and the Volume down 
                                            button to select it. You’ll see an image of an Android robot with a red exclamation mark and the words "No command." 
                                            While holding down the Power button, press the Volume up button and release it. Use the volume buttons to scroll to 
                                            "wipe data/factory reset" and press the Power button to select it. Scroll down to "Yes - erase all user data" and press 
                                            the Power button to select it. If your phone becomes unresponsive at any point during these steps, you can restart it by 
                                            holding down the Power button for several seconds. If that doesn’t work, try taking the battery out and putting it back 
                                            in and starting the steps again.
                                        </p>
                                    </li>
                                </ol>
