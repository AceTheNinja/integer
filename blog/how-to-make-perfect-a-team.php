                                <p>In the early days of running your own business, it's natural to try to do as much as possible yourself. It's the most 
                                    cost-effective, comfortable, sensible way to do things in the beginning. But as your <a>STARTUP</a> grows, you'll
                                    find yourself stretched thinner and thinner. Eventually, you'll find you just can't continue to oversee operations
                                    and sales and accounting and fulfillment and marketing--and hope to continue to grow your business.
                                </p>
                                <p>
                                    When you reach this point, it's time to think about bringing other members on board to help you out. You need to 
                                    build a A-team that's able to manage all the critical areas of your business to take it to the next level.
                                </p>
                                <p>But as it thing comes with each advantage and disadvantage,so now you need to manage all the brains in a positive way.
                                    As each person have different mindset so there will be a chance that your team is not working as a single team due to certain 
                                    factor,so as a Founder you have to rectify such situation before that hurt you startup.
                                </p>
                                <h2>The perfect startup needs a complementary team : </h2>
                                <ul>
                                    <li>It needs a passionate and driven visionary who is the product person.</li>
                                    <li>It needs a capable execution skill that can deliver the product or service against that vision.</li>
                                    <li>It needs people skill to make sure that the best people are recruited and retained, and so that conflict
                                        in the company is resolved.
                                    </li>
                                    <li>It needs administrative skill to make sure that as the company grows the wheels stay on (this skill can
                                        come a bit later – it's not needed on day 1)
                                    </li>
                                </ul>
                                <p>
                                    These skills do not need to be present in 4 distinct people, but most often it takes at least 2, and usually 3
                                    or 4 to lead these areas.Looking at the success factors in more than 100 companies ,
                                    the strongest correlation to success has been the founding team – much more than the idea, or the amount 
                                    of money raised, or almost anything else we can think of.  The best successes came when there were at least
                                    two strong people, with opposite but complementary skills, who had a great deal of mutual trust and respect
                                    for another.  That usually required significant previous experience and time together to build.  
                                </p>

                                <h2>Perfect A-Team Must have : </h2>
                                <ol>
                                    <li><a>Co-founder</a>
                                        <ul>
                                            <li>Person who understands how to build technologies and systems to solve problems;</li>
                                            <li>He/She understands the human factors behind those problems, why they exist, what it takes to fix them and how to shape the experience;</li>
                                            <li>Person who understands how to reach, talk to and sell to the people whose problems are being solved - and keep finding more of them</li>
                                        </ul>
                                    </li>
                                    <li><a>Growth Hacker : </a>
                                        <p>
                                            It is the most important role in a startup: the hacker. This is the person that is most technical. The hacker
                                            loves to chat about new technology stacks, he/she has been using GitHub since it's early days and the most 
                                            visited site on their computer is likely Stack Overflow.
                                            This role is especially important in the early days because without a product the hustler can't sell and without
                                            a backend to tie into, the designer's front-end is useless. Here are five things the hacker should be focused on:
                                        </p>
                                        <ul>
                                            <li>Innovative Problem Solvers - They are constantly thinking of new ideas and ways to tackle problems. They are asking tricky questions to themselves and keenly look out for
                                                inventive answers.
                                            </li>
                                            <li>As Tech Savvy Professional - A good growth hacker is a tech-savvy too, and plays a very crucial 
                                                role as he is on top of new technologies, products, services and communities.
                                            </li>
                                            <li>Being Cut-throat & Confident - In the startup world, there is no space for any doubt; therefore, 
                                                growth hackers are very cut-throat, confident and optimists, who strictly believe that the major
                                                win is just around the corner
                                            </li>
                                        </ul>
                                    </li>
                                    <li><a>Designer : </a>
                                        <p>
                                            A designer in a startup shouldn't just know what looks good. Designers today have to be technical in front-end development
                                            and understand human-centered design approaches. In short, it doesn't suffice to just use Photoshop when you have an entire
                                            product to build!
                                            Here are five things the designer should be focused on:
                                        </p>
                                        <ul>
                                            <li>Design the Product - The designer should work with the hustler to design the "must have" features you decide on as a team. 
                                            </li>
                                            <li>Building a Landing Page - I probably don't need to tell you that the majority of people discover new products on the 
                                                internet, but I will anyway. Because of that, it is incredibly important to build a landing page that shows what you're 
                                                building. The page should include a product description, beautiful images or screenshots, pricing (if applicable),
                                                an about page, a link to your blog and most importantly a place to capture email addresses of interested visitors.
                                            </li>
                                            <li>Test those designs - Once the designer has built mockups, he/she should put those designs in front of real users or customers.
                                            </li>
                                        </ul>
                                    </li>
                                    <li><a>Hustler : </a>
                                        <p>
                                            Every team needs a visionary that is really good at one thing: selling. Whether it's selling the idea to investors, or
                                            pitching your product to customers, the hustler's role is integral to the success of any company.
                                            Many people think of this role as someone with an MBA, but nothing could be further from the truth. A hustler should be
                                            hungry, curious and willing to do anything and everything to move the business forward. Here are five things the hustler 
                                            should be focusing on in the very early stages:
                                        </p>

                                        <ul>
                                            <li>Customer Development - In the early days the hustler should be talking to customers and prospecting. 
                                                Your goal should be to talk to at least 10 people everyday when you are doing customer development.
                                            </li>
                                            <li>Selling - After you've done customer development you should focus on selling your product or 
                                                if you're building a consumer app the focus should be on fundraising.
                                            </li>
                                            <li>Recruiting - Assuming that your business is growing, the hustler should be focused on recruiting.
                                                While this person may not be able to interview for technical skills they can set up interviews and 
                                                decide whether applicants are a good culture fit during the first call.
                                            </li>
                                        </ul>
                                    </li>
                                </ol>

