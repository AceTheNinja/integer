                            <p class="regular-text">
                                The main audiences of this course are an IT professional that have basic knowledge of programming and databases who wants to start with NoSQL technology through MongoDB. Since the course focus on beginners and explain every basic concept there is no business requirements needed to be known before the start of the course.  
                            .  The prerequisites for the MongoDB Certification course are:
                            <ul> 
                                <li>Fundamental knowledge of any programming language</li>
                                <li>Basic understanding of any database, SQL, and query language for databases</li>
                                <li>Working knowledge of Linux or Unix based systems (not mandatory)</li>
                                </ul>
                            It is recommended to do at least one of the following two courses.  As it provides an excellent foundation for the following courses as well.
                            <ul> 
                                <li>Big Data Hadoop Developer</li>
                                <li>Big Data Hadoop Administrator</li>
                                </ul>
                            </p>
