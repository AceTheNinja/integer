                                <h2>How to start <a>Web Development</a> : </h2>
                                <p>As the internet has become a vital part of people's daily lives, web development has become one of the most in-demand
                                    skills. Companies today are looking for web developers to build and maintain websites to meet the wants and needs of
                                    their online customers. You can learn how to code for the web without going through the traditional college degree 
                                    route. There are great free resources online. We've gathered the best ones to guide you on your journey. The best 
                                    way to learn web development is by building something that interests you. As you develop coding skills, you will 
                                    understand what is possible and that will inspire you to create your own project.
                                </p>
                                <p>
                                    By applying your skills on an interesting project, you will enjoy the learning process more and learn at a faster pace. 
                                    So start working on your own project as soon as possible while continuing to gain more skills by following this roadmap. 
                                    Learning how to code is similar to learning a new language. The best language students don't just take courses, they also 
                                    immerse themselves in the new culture by spending a lot of time with native speakers. In the same way, we encourage you 
                                    to immerse yourself in web development by building a site that showcases your creativity and personality. Remember, if 
                                    it's interesting to you, it will be easier and faster to learn. Not a game developer, an iOS developer or a Windows 
                                    programmer. This path is designed to teach web development, focusing on building online apps. Still, if you chose to 
                                    then move on to another type of programming, you will be able to re-use most of what you will learn if you follow this 
                                    proposed path. You will need books. Prepare a budget for this. There are tons of freely available resources on the web 
                                    but nothing beats a structured learning path, delivered by an experienced professional author in a consistent way. You 
                                    will obviously need a computer. 
                                </p>
                                <p>
                                    Mac, Windows, Linux, it doesn't matter. You should be able to follow these steps regardless of your chosen operating system.
                                    If you are on a schedule and don't care about the intro, just jump at the section titled 'Where to start?'. Let's start by 
                                    clearing out a misconception. Writing HTML (and CSS) is not programming. If you can create static web pages using a text 
                                    editor, great: you already possess some of the needed skills. Nonetheless, this doesn't make you a developer. HTML, as the 
                                    acronym implies, is a markup language. That's what the M stands for. It is not a programming language. HTML and CSS allow 
                                    you to describe the layout of a document (in this case, a web page) but it doesn't let you create something dynamic like 
                                    validating a username or retrieving search results from a database. If coding HTML was considered programming, typing a 
                                    letter using Microsoft Word would be too. In the strict sense, a programming language is a way to issue instructions to a 
                                    computer in a way that is both readable by a human and understandable by the said computer. To qualify as a programming 
                                    language, it must be capable of implementing some kind of 'control logic' such as 'if you get this value, do this and if 
                                    not, do that'. To know more about what a programming language is, read the Wikipedia page. Because HTML is not capable of 
                                    modeling algorithms, it is not a programming language. 
                                </p>
                                <p>
                                    Suppose that if you want to learn programming, it is because you have a purpose for this adventure. Most of the persons 
                                    asking me about learning programming have some ideas about building a web application (the next Facebook, most likely). 
                                    To be a web developer, you will need to master quite a few concepts, such as user interface design, server side programming,
                                    client side programming, utilizing a database, etc. Learning to develop web applications is hard. Don't doubt it. Forget 
                                    about all the outlets promising you to become a developer in 24 hours, a week, a month. Forget about the ones that tell 
                                    you things like 'this is the only thing you'll need to learn'. You will need to read. A lot. You will need to try out what 
                                    you read. A lot more. Never forget that web development is a profession. Web developers get hired and usually make a decent
                                    salary out of their trade because it is difficult. If it was quick and easy, we'd see a lot more developers around.
                                </p>

