                                 This 90-minute exam consists of 65–75 questions and covers integration of Intrusion Prevention System (IPS) and 
                                 context-aware firewall components, as well as Web (Cloud) and Email Security solutions. Candidates can prepare 
                                 for this exam by taking the Implementing Cisco Threat Control Solutions (SITCS) course.  
                            </p>
                            <p>
