                                <h2>How to start <a>Android Development</a> : </h2>
                                <p>There’s a lot to get excited about in mobile application development today. With increasingly sophisticated hardware
                                    , tablet PCs and a variety of software platforms (Symbian OS, iOS, WebOS, Windows Phone 7), the landscape for mobile
                                    developers is full of opportunities — and a little complex as well. So much choice can be overwhelming when you just
                                    want to get started building mobile applications. Which platform should you choose? What programming language should 
                                    you learn? What kit do you need for your planned project? In this tutorial, you’ll learn how to start writing 
                                    applications for Android, the open-source mobile operating system popularized by Google. Android is an open-source 
                                    platform based on the Linux kernel, and is installed on thousands of devices from a wide range of manufacturers. 
                                    Android exposes your application to all sorts of hardware that you’ll find in modern mobile devices — digital 
                                    compasses, video cameras, GPS, orientation sensors, and more.
                                </p>
                                <p>
                                    Android’s free development tools make it possible for you to start writing software at little or no cost. When you’re 
                                    ready to show off your application to the world, you can publish it to Google’s Android Market. Publishing to Android 
                                    Market incurs a one-off registration fee (US $25 at the time of writing) and, unlike Apple’s App Store which famously 
                                    reviews each submission, makes your application available for customers to download and buy after a quick review process
                                    — unless the application is blatantly illegal. The Android SDK is available for Windows, Mac and Linux, so you don’t 
                                    need to pay for new hardware to start writing applications. An SDK built on Java. If you’re familiar with the Java 
                                    programming language, you’re already halfway there. By distributing your application on Android Market, it’s available 
                                    to hundreds of thousands of users instantly. You’re not just limited to one store, because there are alternatives, too. 
                                    For instance, you can release your application on your own blog. Amazon has recently been rumored to be preparing their 
                                    own Android app store also. As well as the technical SDK documentation, new resources are being published for Android 
                                    developers as the platform gains popularity among both users and developers. 
                                </p>
                                <p>
                                    That said, each language has its strong and weak points. In my opinion, a great language to have an understanding of is 
                                    Java. It can be summed up into one, simple sentence: Java is a mature language. Due to how long Java has been around, 
                                    almost any question you can imagine has already been asked, answered, indexed, and democratically perfected through up 
                                    votes on the Internet. It is seriously hard to stump a search engine with a Java coding problem. Java has a very rich API, 
                                    and an incredible supporting open source ecosystem. There are tools upon tools for just about everything you would like to 
                                    do. There’s also an amazing community driven process that ensures growth in the right direction. Java is an Object Oriented 
                                    language. It internally embraces best practices of object oriented design and strongly suggests that you learn and follow 
                                    them. It also heavily promotes correct usage and many of the documented Design Patterns use Java as the language de facto. 
                                    Understanding design patterns can lead to much more maintainable code. 
                                </p>
                                <p>
                                    The recommended environment for developing Android applications is Eclipse with the Android Development Toolkit (ADT) 
                                    plugin installed. I’ll summarize the process here. If you need more detail, Google’s own developer pages do a good job of 
                                    explaining the installation and configuration process. Download the Android SDK for your platform (Windows, Mac OS X, or 
                                    Linux). Extract the downloaded file to somewhere memorable on your hard drive (on Linux, I use /opt/local/). If you don’t 
                                    already have Eclipse installed, download and install the Eclipse IDE for Java Developers package. 
                                </p>
                                <p>
                                    For programming, Google recommends using Eclipse 3.5 (Galileo). Run Eclipse and choose Help->Install New Software. 
                                    Click Add in the Available Software window. Enter Android Development Tools in the Name field, and 
                                    https://dl-ssl.google.com/android/eclipse/ in the Location field. Click OK and check Developer Tools in the list of 
                                    available software. This will install the Android Development Tools and DDMS, Android’s debugging tool. Click Next and 
                                    Finish to install the plugin. You’ll need to restart Eclipse once everything is installed. When Eclipse restarts, choose 
                                    Window->Preferences and you should see Android listed in the categories. You now need to tell Eclipse where you’ve 
                                    installed the Android SDK. Click Android and then Browse to select the location where you extracted the SDK files. 
                                    For example, /opt/local/android-sdk. Before you can start writing applications for Android, you need to download the 
                                    SDK platforms for the Android devices for which you want to develop apps. Each platform has a different version of the 
                                    Android SDK that may be installed on users’ devices. For versions of Android 1.5 and above, there are two platforms 
                                    available: Android Open Source Project and Google. The Android Open Source Project platforms are open source, but do 
                                    not include Google’s proprietary extensions such as Google Maps. If you choose not to use the Google APIs, Google’s 
                                    mapping functionality won’t be available to your application. Unless you have a specific reason not to, I’d recommended 
                                    you to target one of the Google platforms, as this will allow you to take advantage of Google’s proprietary extensions. 
                                    Choose Window->Android SDK and AVD Manager. Click Available Packages in the left column and check the repository to show 
                                    a list of the available Android platforms. You can choose which platforms to download from the list, or leave everything 
                                    checked to download all the available platforms. When you’re done, click Install Selected and follow the installation 
                                    instructions.
                                </p>                    
