                                    picking up other languages becomes simple. HTML, Javascript, C, C++, C#, all become much easier programming languages to
                                    grasp once you've got the basics down. When you learn a new foreign spoken language, you notice that learning other spoken
                                    languages becomes a lot easier. 

                                </p>
                                <p>
                                    The same is true with programming. Once you learn how to think like a programmer and know a programming language or two, 
                                    picking up other languages becomes simple. HTML, Javascript, C, C++, C#, all become much easier programming languages to 
                                    grasp once you've got the basics down. Let’s get one thing clear: learning how to program opens up endless opportunities 
                                    and has endless benefits. You can learn it from the comfort of your own home, the job market wants and needs you, and it’s 
                                    fun. Now, there are lots of programming languages out there to choose from, and I don’t believe in competition between them. 
                                    All languages were created for a specific reason, and have evolved and contributed in some way to the technological advances 
                                    of our times. 
                                    Pitting them against each other usually ends up watering down the main reason the language was conceived.
                                </p>
                                <p>
                                    That said, each language has its strong and weak points. In my opinion, a great language to have an understanding of is 
                                    Java. It can be summed up into one, simple sentence: Java is a mature language. Due to how long Java has been around, 
                                    almost any question you can imagine has already been asked, answered, indexed, and democratically perfected through up 
                                    votes on the Internet. It is seriously hard to stump a search engine with a Java coding problem. Java has a very rich API, 
                                    and an incredible supporting open source ecosystem. There are tools upon tools for just about everything you would like to 
                                    do. There’s also an amazing community driven process that ensures growth in the right direction. Java is an Object Oriented 
                                    language. It internally embraces best practices of object oriented design and strongly suggests that you learn and follow 
                                    them. It also heavily promotes correct usage and many of the documented Design Patterns use Java as the language de facto. 
                                    Understanding design patterns can lead to much more maintainable code. 
                                </p>
                                <p>
                                    The IDEs available for Java will blow your mind. Due to its strong typing, you’ll not only be notified immediately of errors,
                                    but you’ll also be given suggestions that will refactor and reformat your code with clear explanations and extreme ease. 
                                    After using them, most people wonder how they ever coded before. Java is running just about everywhere you can imagine. 
                                    It’s usually where most large applications end up due to its scalability, stability, and maintainability. There’s also 
                                    currently a gigantic push in the Java community to be the leader of the IoT (Internet of Things). And it’s coming. 
                                    Very fast. There’ll be a time in the near future when your alarm clock will automatically start brewing your coffee pot, 
                                    and it’ll most likely be Java doing that. There are tons of open positions waiting for you due to Java’s widespread reach. 
                                    Many different sectors embrace the language, and you can be pretty certain you’ll land a job in just about any job market
                                    you’d like. All Android Apps are written in Java. By taking our Android Development track, you can publish that app you’ve 
                                    always dreamed of building. Java is a verbose language, which at first can seem daunting. 
                                </p>
                                <p>
                                    However, after learning the basics you’ll find that you can easily grab onto more advanced concepts because the code is very 
                                    explicit. Plus, there are great courses (wink, wink, nudge, nudge) out there that can ease you into this powerful language.
