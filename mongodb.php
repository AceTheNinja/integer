                            <ul class="student_learn">
                           
                                <li>Creating Android apps for mobile devices </li>
                                <li>Setting up the IDE to develop and test Android apps for smartphones and tablets</li>
                                <li>Using XML-based layouts to place buttons, checkboxes, images and other widgets on the screen</li>
                                <li>Managing different screen sizes and phone/tablet rotation</li>
                                <li>Accepting keyboard input, as well as responding to screen touches, touch-and-holds and swipes</li>
                                <li>Displaying pop-up messages and notifications</li>
                                <li>Managing the lifecycle of an app, including when it's interrupted by a phone call</li>
                                <li>Embedding SQLite databases in apps for persistent storage</li>
